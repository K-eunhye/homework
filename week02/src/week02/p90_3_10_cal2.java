package week02;
//멘토씨리즈 자바 예제 코드작성 p90
public class p90_3_10_cal2 {
	public static void main(String[] args) {
		
		double num1 = 1.2345; // 실수형double타입 변수선언후 값 초기화
		int num2= 6; //정수형 int 타입 변수 선언 후 값 초기화
		System.out.println(num1 + num2);
		//실수형+정수형을 계산하면 자동으로 실수형 결과값이 나온다. 결과 7.2345
		
		
		
	}

}
