package week02;
//멘토씨리즈 자바 예제 코드작성 p97
public class p97_3_16_threeCal {
	public static void main(String[] args) {
		
		int num = (7 > 1) ? 1 : 2;
		//정수형 int타입 변수 num에 값을 대입하겠다!
		// 만약 (7이 1보다 큰게) ? 참이면 1 : 거짓이면 2를 선택하여 출력하겠다.
		System.out.println(num); // ()안의 값이 참이었으므로 1이 변수에 저장되어 출력되었다.
		
	}

}
