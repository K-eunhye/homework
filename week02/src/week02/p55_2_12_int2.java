package week02;

///멘토씨리즈 자바 예제 코드작성 p55

public class p55_2_12_int2 {
	public static void main(String[] args) {
		
		byte num1 = 0110; //정수형 byte타입 변수 선언하고 2진법으로 값을 넣어 초기화함
		short num2 = 0107;//정수형 short타입 변수 선언하고 8진법으로 값을 넣어 초기화함
		int num3 = 0x46;//정수형 int타입 변수 선언하고 16진법으로 값을 넣어 초기화함
		long num4 = 69L; ///정수형 long타입 변수 선언하고 롱타입에 맞게 뒤에 L로 값을 넣어 초기화함
		
		System.out.println(num1); //변수 num1에 있는 값 콘솔창에 출력 = 10진법에 맞춰나옴
		System.out.println(num2); //변수 num2에 있는 값 콘솔창에 출력 = 10진법에 맞춰나옴
		System.out.println(num3); //변수 num3에 있는 값 콘솔창에 출력 = 10진법에 맞춰나옴
		System.out.println(num4); //변수 num4에 있는 값 콘솔창에 출력 = 10진법에 맞춰나옴
		
		
		
		
	}
}
