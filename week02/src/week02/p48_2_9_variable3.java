public class p48_2_9_variable3 {
	public static void main(String[] args) {
		
		int myAge = 20; //정수형 변수 myAge 선언하자마자 20이라는 값을 넣어 초기화 시킴
		int yourAge = myAge; //정수형 변수 yourAge를 선언하자 마자 myAge의 값을 넣어 초기화 시키는데 그 값이 20 이므로 
								//yourAge와  myAge의 값은 같음
		
		System.out.println(myAge); //myAge에 있는 값 출력
		System.out.println(yourAge); //yourAge에 있는 값 출력
	}

}
