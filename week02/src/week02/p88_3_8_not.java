package week02;
//멘토씨리즈 자바 예제 코드작성 p88
public class p88_3_8_not {
	public static void main(String[] args) {
		
		boolean isHuman = false; // 논리형 boolean타입 변수 선언 후 false값으로 초기화함
		System.out.println(!isHuman); // isHuman는 true와 false값만 나오게 되어 있으므로 false를 부정한다면 true값이 나옴
		System.out.println(isHuman);//변수에 저장되어 있는 false값이 나옴
		
	}

}
