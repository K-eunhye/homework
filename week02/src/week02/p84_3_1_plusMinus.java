package week02;
//멘토씨리즈 자바 예제 코드작성 p84
public class p84_3_1_plusMinus {
	public static void main(String[] args) {
		
		int x = 100; //정수형int타입 변수 선언후 100을 값으로 넣어 초기화
		int resltPlus = +x; // int타입 변수 선언후 +100을 값으로 넣음
		int resltMinus = -x; // int타입 변수 선언후 -100을 값으로 넣음
		
		System.out.println(resltPlus); // 100이라는 결과가 나옴 +는 원래 기호를 표기하지 않는게 기본이니까!
		System.out.println(resltMinus);  //-100이라는 결과가 나옴
		
		
	}

}
