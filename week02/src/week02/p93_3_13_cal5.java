package week02;
//멘토씨리즈 자바 예제 코드작성 p93
public class p93_3_13_cal5 {
	public static void main(String[] args) {
		
	int a = 10; //정수형 int타입 변수 선언 후 10저장
	int b = 20;//정수형 int타입 변수 선언 후 20저장
	
	//관계연산자란 두 숫자나 식의 관계를 표현하는 것이다. 표현한값이 맞으면 true 틀리면 false로 출력된다
	System.out.println(a > b); // 10 > 20 = false
	System.out.println(a <= b); // 10 <= 20 = true
	System.out.println(a == b); // 10 == 20 = false
	System.out.println(a != b); // 10 != 20 = true
	
	}

}
