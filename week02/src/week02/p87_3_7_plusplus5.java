package week02;
//멘토씨리즈 자바 예제 코드작성 p87
public class p87_3_7_plusplus5 {
	public static void main(String[] args) {
		
		char alphabetX = 'X'; //문자형char타입으로 변수 선언후 X문자 저장. 유니코드로 변환도 가능
		System.out.println(alphabetX++); // X의 값출력하고 나서 유니코드에 1을 증가시킴
		System.out.println(alphabetX); // X에서 1이 커진 Y로 출력이된다
		
	}

}
