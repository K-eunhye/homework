package week02;
//멘토씨리즈 자바 예제 코드작성 p87
public class p87_3_6_plusplus4 {
	public static void main(String[] args) {
		
		int x = 1; //정수형 int타입 변수 선언 후 1을 넣어 초기화
		System.out.println(x++); // x값 1을 출력하고 그 이후에 1을 증가시킨다.
		System.out.println(x); // 이미 x에 더하기가 되어있으므로 2 값이 출력된다
		System.out.println(++x); // x값 2에 1을 먼저더하고 더한 결과 3을 출력
		
	}

}
