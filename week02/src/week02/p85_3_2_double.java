package week02;
//멘토씨리즈 자바 예제 코드작성 p85
public class p85_3_2_double {
	public static void main(String[] args) {
		
		
		double d = 1.11; //실수형 double타입 변수 선언 후 1.11값을 넣어 초기화
		double result = -d; // 실수형 double타입 변수 선언 후 1.11값을 -붙여서 초기화
		
		System.out.println(-d); // 1.11에 -를 붙여 -1.11로 나옴
		System.out.println(result); // 이미 -1.11이 들어있으므로  -1.11로 나옴
		
	}

}
