package week02;
//멘토씨리즈 자바 예제 코드작성 p85
public class p85_3_3_plusplus {
	public static void main(String[] args) {
		
		int num = 10; //정수형 int타입 변수 선언 후 10을 넣어 초기화
		System.out.println(num); // num값을 출력하면 10이 그대로 보임
		num++; //num에 증감식을 적용해 1을 더함.
		System.out.println(num); // 1을 더한 값은 11이며 그 값이 출력된다
		
	}

}
