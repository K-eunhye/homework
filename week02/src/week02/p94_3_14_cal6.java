package week02;
//멘토씨리즈 자바 예제 코드작성 p94
public class p94_3_14_cal6 {
	public static void main(String[] args) {
		
		int a = 10; //int타입 변수에 10저장
		int b = 11; //int타입 변수에 11저장
		
		System.out.println(a <= b); // 10 <= 11 = true
		System.out.println(a == b); // 10 == 11 = false
		System.out.println(a != --b); // 10 != 아직 11 = 10은 11이 아니므로 true값이 나옴
		
	}

}
