package week03;
//멘토씨리즈 자바 예제 코드작성 p151
public class p151_6_2_array {
	public static void main(String[] args) {
		

		int[] arr = new int[4]; //4칸짜리 1차원 배열 선언, 안에 값은 아직 없음
		
		arr[0] = 1; //인덱스 0번의 값을 1로 넣음
		arr[1] = 2; //인덱스 1번의 값을 2로 넣음
		arr[2] = 3;//인덱스 2번의 값을 3로 넣음
		arr[3] = 4; //인덱스 3번의 값을 4로 넣음
		
		System.out.println("arr[0] = " + arr[0]);
		System.out.println("arr[1] = " + arr[1]);
		System.out.println("arr[2] = " + arr[2]);
		System.out.println("arr[3] = " + arr[3]);
	}

}
