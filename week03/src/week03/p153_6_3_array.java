public class p153_6_3_array {
	
	//배열 선언과 초기값
	
	public static void main(String[] args) {
		
		int[] intArray = new int[5]; // 5칸짜리 int 타입 1차원 배열 선언
		String[] strArray = new String[5];  // 5칸짜리 String type 1차원 배열 선언
		
		int[] VarArray = {1,2,3,4,5}; //5칸짜리, 값이 다 들어있는 int type 1차원 배열 선언
		
		System.out.println("intArray[0] = " + intArray[0]);
		System.out.println("intArray[1] = " + intArray[1]);
		
		System.out.println("strArray[0] = " + strArray[0]);
		System.out.println("strArray[1] = " + strArray[1]);
		
		System.out.println("VarArray[0] = " + VarArray[0]);
		System.out.println("VarArray[1] = " + VarArray[1]);
		
		//출력결과
//		intArray[0] = 0
//		intArray[1] = 0
//		strArray[0] = 0
//		strArray[1] = 0
//		VarArray[0] = 1
//		VarArray[1] = 2

		
		
		
	}

}
